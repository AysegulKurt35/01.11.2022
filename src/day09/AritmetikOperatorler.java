package day09;

public class AritmetikOperatorler {
    public static void main(String[] args) {

        //ARITMETIK OPERATORLER//
        //
        //Bu operatorler ikilidir.

        // "+" :operand1+operand2 ORNEK:7<-3+4
        int toplam = 3 + 4;
//Statement ve expression
        System.out.println("Toplam =" + toplam);

        //Task:x,y ve z degiskenleri tanimlanacaktir.12,10 ve x+y atamalarini yapiniz.

        int x = 12;
        int y = 10;
        int z = x + y;

        System.out.println("x=" + x + "\ny=" + y + "\nz=" + z);

        //"-"  :operand1-operan2  ORNEK:6<--9-3
        int a = 9;
        int b = 3;
        int sonuc = a - b;

        System.out.println("sonuc=" + sonuc);

        //"*"  :operand1*operand2
        int carpim = 4 * 5;
        System.out.println("4*5=" + carpim);

        //"/"  :operand1/operand2
        int bolum = 8 / 2;
        System.out.println("8/2=" + bolum);


        //"%"  : operand1+operand2 ORNEK:
        int kalan = 5 % 2;
        System.out.println("5 mod 2=" + kalan);


    }





    }




















